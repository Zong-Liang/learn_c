/*
 * +=
 * -=
 * *=
 * /=
 * %=
 */

#include <stdio.h>

int main() {
    int i_1 = 5;
    i_1 += 1;
    printf("the result of i_1 += 1: %d\n", i_1);
    i_1 -= 1;
    printf("the result of i_1 -= 1: %d\n", i_1);
    i_1 *= 2;
    printf("the result of i_1 *= 1: %d\n", i_1);
    i_1 /= 2;
    printf("the result of i_1 /= 1: %d\n", i_1);
    i_1 %= 3;
    printf("the result of i_1 %= 1: %d\n", i_1);
    return 0;
}
